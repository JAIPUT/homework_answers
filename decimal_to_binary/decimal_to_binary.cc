#include <cstdint>
#include <string>

const unsigned char conversion_table[32] = {
    0x49, 0x20, 0x53, 0x48, 0x4F, 0x55, 0x4C, 0x44,
    0x27, 0x56, 0x45, 0x20, 0x44, 0x4F, 0x4E, 0x45,
    0x20, 0x4D, 0x59, 0x20, 0x4F, 0x57, 0x4E, 0x20,
    0x48, 0x4F, 0x4D, 0x45, 0x57, 0x4F, 0x52, 0x4B
};

std::string recursive_convert(const uint32_t val,
                              unsigned int bit_num = 0) {
    if (bit_num >= 32) return "";

    auto cv = conversion_table[bit_num];
    auto ch = ((cv >> 1) & 0x20 & -((val >> ++bit_num) & 1)) + cv;
    return std::string(1, ch) + recursive_convert(val, bit_num);
}
